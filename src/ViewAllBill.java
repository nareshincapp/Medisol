    void allBill(){
     try {
            java.sql.Date d1 =new java.sql.Date(dt1.getDate().getTime());
            java.sql.Date d2 =new java.sql.Date(dt2.getDate().getTime());
            ResultSet rs = st.executeQuery("select * from bill where bdate >='"+d1+"' and bdate<='"+d2+"'");
             
        while(rs.next()){
        
            Vector row= new Vector();
                   row.add(rs.getInt(1));
                    row.add(rs.getInt(3));
                    row.add(rs.getInt(4));
                     row.add(rs.getInt(5));
                      row.add(rs.getString(6));
                    
                   DefaultTableModel  dtm = (DefaultTableModel)jt4.getModel();
                   dtm.addRow(row);
             }
          } catch (Exception ex) {
           JOptionPane.showMessageDialog(null, ex);
          }
    }

void addMember(){
String n=this.n.getText();
     String ph=tf2.getText();
     String mid="";
     String pass="";
     mid =n.substring(0,2)+n.charAt(n.length()-2)+n.charAt(n.length()-1)+
             ph.substring(0,2)+ph.charAt(ph.length()-2)+ph.charAt(ph.length()-1);
     String p="abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ1234567890#@";
     
      try {
     for(int i=0;i<8;i++)
     {
     int index =(int) (0+Math.random()*(p.length()-1)); //0 index
     pass= pass+p.charAt(index);//pass=+p.charAt(index);
     }
             addmember.setString(1,mid);
             addmember.setString(2,pass);
             addmember.setString(3,n);
             addmember.setString(4,ph);
             addmember.executeUpdate();
             JOptionPane.showMessageDialog(null, "member inserted successfully");
         
     
      }catch (Exception ex) {
             JOptionPane.showMessageDialog(null, ex);
         }
}
